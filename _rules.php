<h1>Rules</h1>

<h4>Dates and Times</h4>
<ol class="rules">
  <li>Doors open at <strong>2:00 PM</strong> for registration on
      <strong>Saturday, April 6.</strong></li>
  <li>At <strong>4:00 PM sharp</strong>, the theme of the competition will be
      revealed, and the competition will start.</li>
  <li>At <strong>11:00 PM on Saturday</strong>, the competition will break, and
      will resume at <strong>9:00 AM on Sunday</strong>.
  <li>The competition ends at <strong>2:00 PM on Sunday, April 7</strong>.
      Construction must end.</li>
  <li>The judging ceremony will be at <strong>3:30 PM on Sunday</strong>.</li>
</ol>

<h4>Teams</h4>
<ol class="rules">
  <li>Teams should consist of 3 or 4 members.</li>
  <li>One team member should be designated the team captain. He/she is
      responsible for all communications between the team and the organizers.</li>
  <li>Each member must sign a liability waiver before the start of the competition.</li>
  <li>There is a $5.00 entry fee per person.</li>
</ol>

<h4>The Design</h4>
<ol class="rules">
  <li>To build their machines, we will provide tools and materials to all teams.</li>
  <li>Some teams may want to spend additional money on specific items;
    out of pocket spending is permitted up to an amount of $25 per team.</li>
  <li>Designs are constrained to a 1m x 1m base. No part of the device is
    allowed to extend beyond this limit during operation. However, there is no
    height limit.</li>
  <li>Teams whose machine involves hazardous materials, explosives, chemical
    reactions leading to high temperatures, fire, or live animals may be asked
    to revise their design.</li>
</ol>

<h4>Judging and Prizes</h4>
<ol class="rules">
  <li>The designs will be judged by three SEAS professors. The judges&#39;
    decisions are final.</li>
  <li>Designs will be judged based on creativity, effectiveness, and complexity.</li>
  <li>The three best designs overall will receive prizes.</li>
</ol>

